    <h2>
A Direção Escolar é o departamento executivo encarregado da organizar as atividades administrativas e pedagógicas da Etec. Suas atribuições, indicação e designação, são realizadas de acordo com o Capítulo IV, Seção I do Regimento Comum das Etecs (Deliberação CEETEPS nº 87, de 08 de dezembro de 2022).<br>
<br>
Atribuições da Direção Escolar<br>
De acordo com o artigo 20 do Regimento Comum das Etecs, o Diretor de Etec, sem prejuízo de outras constantes em documento próprio do CEETEPS e da legislação, terá as seguintes atribuições:<br>
<br>
garantir as condições para o desenvolvimento da gestão democrática do ensino, na forma prevista pela legislação e neste Regimento;<br>
promover a elaboração do projeto político-pedagógico da escola;<br>
coordenar a elaboração, o acompanhamento, a avaliação e o controle da execução do Plano Plurianual de Gestão;<br>
gerenciar os recursos físicos, materiais, humanos e financeiros para atender às necessidades da escola a curto, médio e longo prazos;<br>
coordenar o planejamento, execução, controle e avaliação das atividades da escola com o objetivo de garantir a melhoria dos processos;<br>
garantir o cumprimento dos conteúdos curriculares, das cargas horárias e dos dias letivos previstos na legislação vigente;<br>
garantir os meios para a recuperação de alunos de menor rendimento e em progressão parcial, seja ela na forma presencial ou virtual;<br>
assegurar o cumprimento da legislação, bem como dos regulamentos, diretrizes e normas emanadas da administração superior e da Administração Central do CEETEPS;<br>
expedir diplomas, certificados e outros documentos escolares, responsabilizando-se por sua autenticidade e exatidão;<br>
promover estudos fundamentados por indicadores institucionais e gerenciar ações visando à oferta de cursos e seu contínuo aprimoramento, assim como de programas, dos recursos físicos, materiais e humanos que compõem a escola;<br>
administrar e responsabilizar-se pelo patrimônio do CEETEPS, observadas as normas e diretrizes legais e infralegais aplicáveis, fomentando estratégias para controle, manutenção, preservação e desfazimento dos bens inservíveis;<br>
promover ações para a integração escola-família e comunidade-empresa, apoiado em indicadores de fontes reconhecidas;<br>
coordenar a elaboração de projetos, submetendo-os à aprovação dos órgãos competentes, acompanhar seu desenvolvimento e avaliar seus resultados;<br>
criar condições e estimular experiências inovadoras para o aprimoramento do processo educacional;<br>
integrar as ações dos serviços prestados pela escola;<br>
criar procedimentos que favoreçam a comunicação e o fluxo de informações junto à Comunidade Escolar;<br>
convocar para as reuniões ordinárias e extraordinárias, de forma presencial ou virtual, os representantes dos órgãos colegiados discentes, docentes, servidores e membros dos demais segmentos da comunidade escolar;<br>
desempenhar outras atividades correlatas e afins.<br>
